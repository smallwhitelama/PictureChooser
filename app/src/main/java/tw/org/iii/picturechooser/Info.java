public class Info {
    public String x;
    public String y;
    public String z;
    int gray;
    public void setX(String x) {
        this.x = x;
    }
    public void setY(String y) {
        this.y = y;
    }
    public void setZ(String z) {
        this.z = z;
    }
    public void setGray(int gray) {
        this.gray = gray;
    }
    public String getX() {
        return x;
    }
    public String getY() {
        return y;
    }
    public String getZ() {
        return z;
    }
    public int getGray() {
        return gray;
    }
}
